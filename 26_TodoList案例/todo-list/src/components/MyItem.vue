<template>
  <li>
    <label>
      <input
        type="checkbox"
        :checked="todo.done"
        @change="handleCheck(todo.id)"
      />
      <!-- 不推荐的写法：虽然更方便，但修改了props传入的属性，违反原则
       -->
      <!-- <input type="checkbox" v-model="todo.done" /> -->
      <span>{{ todo.name }}</span>
    </label>
    <button class="btn btn-danger" style="display: none">删除</button>
  </li>
</template>

<script>
export default {
  name: "MyItem",
  props: ["todo", "checkTodo"],
  // 声明接收todo对象
  methods: {
    handleCheck(id) {
      // 通知App组件将对应的todo对象的done值取反
      this.checkTodo(id);
    },
  },
};
</script>

